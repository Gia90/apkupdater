package com.apkupdater.model;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import android.os.Parcel;
import android.os.Parcelable;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

public class Update
	implements Parcelable
{
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	private String mName;
	private String mPname;
	private String mVersion;
	private String mUrl;

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public Update(
		InstalledApp app,
	    String url
	) {
		setName(app.getName());
		setPname(app.getPname());
		setVersion(app.getVersion());
		setUrl(url);
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public String getName(
	) {
		return mName;
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public void setName(
		String mame) {
		mName = mame;
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public String getPname() {
		return mPname;
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public void setPname(String pname) {
		mPname = pname;
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public String getVersion() {
		return mVersion;
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public void setVersion(String version) {
		mVersion = version;
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public String getUrl() {
		return mUrl;
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public void setUrl(String mUrl) {
		this.mUrl = mUrl;
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	@Override
	public int describeContents(
	) {
		return 0;
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	@Override
	public void writeToParcel(
		Parcel dest,
		int flags
	) {
		dest.writeString(mName);
		dest.writeString(mPname);
		dest.writeString(mVersion);
		dest.writeString(mUrl);
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	protected Update(
		Parcel in
	) {
		mName = in.readString();
		mPname = in.readString();
		mVersion = in.readString();
		mUrl = in.readString();
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	@SuppressWarnings("unused")
	public static final Parcelable.Creator<Update> CREATOR = new Parcelable.Creator<Update>() {
		@Override
		public Update createFromParcel(Parcel in) {
			return new Update(in);
		}

		@Override
		public Update[] newArray(int size) {
			return new Update[size];
		}
	};

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
